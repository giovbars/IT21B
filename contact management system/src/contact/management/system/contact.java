    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
                        
    // Existing code
    int selectedRow = jTable1.getSelectedRow();

    if (selectedRow != -1) {
        String firstName = jTextFieldFname.getText();
        String lastName = jTextFieldLname.getText();
        String phone = jTextFieldPhone.getText();
        String address = jTextAreaAddress.getText();

        if (firstName.isEmpty() || lastName.isEmpty() || phone.isEmpty() || address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All fields (First Name, Last Name, Contact, and Address) must be filled out.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setValueAt(firstName, selectedRow, 0);
        model.setValueAt(lastName, selectedRow, 1);
        model.setValueAt(phone, selectedRow, 2);
        model.setValueAt(address, selectedRow, 3);

        // Added Code: Sort contacts after updating a contact
        sortContacts();
    } else {
        JOptionPane.showMessageDialog(this, "Please select a contact to update.");
    }


    }//GEN-LAST:event_jButtonUpdateActionPerformed

