    private void jButtonAddContactActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddContactActionPerformed
    
    String firstName = jTextFieldFname.getText().trim();
    String lastName = jTextFieldLname.getText().trim();
    String phone = jTextFieldPhone.getText().trim();
    String address = jTextAreaAddress.getText().trim();

    if (firstName.isEmpty() || lastName.isEmpty() || phone.isEmpty() || address.isEmpty()) {
        JOptionPane.showMessageDialog(this, "All fields (First Name, Last Name, Contact, Address) must be filled!", 
                "Missing Information", JOptionPane.ERROR_MESSAGE);
        return;
    }

    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.addRow(new Object[]{firstName, lastName, phone, address});

    jTextFieldFname.setText("");
    jTextFieldLname.setText("");
    jTextFieldPhone.setText("");
    jTextAreaAddress.setText("");
    sortContacts();
    }//GEN-LAST:event_jButtonAddContactActionPerformed

