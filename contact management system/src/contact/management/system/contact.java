    private void searchbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbuttonActionPerformed
     // Get the search query from the search bar
    String query = searchbar.getText().trim().toLowerCase(); // Case-insensitive search
    
    if (query.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please enter a search query.", "Empty Search", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    // Reset all rows' selection before performing a new search
    jTable1.clearSelection();

    // Get the table model and number of rows/columns
    javax.swing.table.TableModel model = jTable1.getModel();
    int rowCount = model.getRowCount();
    int colCount = model.getColumnCount();
    
    boolean found = false;

    // Loop through the rows and columns to search for the query
    for (int i = 0; i < rowCount; i++) {
        for (int j = 0; j < colCount; j++) {
            Object cellValue = model.getValueAt(i, j);
            if (cellValue != null && cellValue.toString().toLowerCase().contains(query)) {
                // Highlight the row where the query is found
                jTable1.addRowSelectionInterval(i, i);
                found = true;
            }
        }
    }
    
    if (!found) {
        JOptionPane.showMessageDialog(this, "No matching records found.", "Search Result", JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_searchbuttonActionPerformed

